            <h2 class="anchor page-title" id="history"><span class="text-color-title">Ключевые даты</span> в истории
                ГУАП
                <a class="link-anchor" href="#history" title="#history"><i class="fa fa-anchor"></i></a>
            </h2>
            <p><b>25 января 1941 года</b> Совет народных комиссаров Союза Советских Социалистических Республик принял
                постановление о создании Ленинградского авиационного института (ЛАИ). Эта дата является официальным днем
                рождения вуза.</p>
            <p><b>20 февраля 1945 года</b> было принято Постановление Правительства о возобновлении деятельности
                Ленинградского авиационного института и преобразовании его в Ленинградский институт авиационного
                приборостроения (ЛИАП).</p>
            <p><b>В 1992 году</b> ЛИАП успешно прошел государственную аттестацию, по итогам которой он был преобразован
                в
                государственную академию аэрокосмического приборостроения (ГААП).</p>
            <p><b>В 1998 году</b> вуз прошел очередную государственную аттестацию, получил статус университета и
                наименование Санкт-Петербургский государственный университет аэрокосмического приборостроения (ГУАП).
            </p>
            <p><b>В 2011 году</b> вуз был преобразован в федеральное государственное автономное образовательное
                учреждение
                высшего профессионального образования «Санкт-Петербургский государственный университет аэрокосмического
                приборостроения».</p>
            <p><b>В 2015 году</b> вуз был переименован в федеральное государственное автономное образовательное
                учреждение
                высшего образования «Санкт-Петербургский государственный университет аэрокосмического приборостроения».
            </p>

